      subroutine MATPRIN (M,N1,N2,MM,NN,X)
      real X(MM,NN)
      automatic K,I
      
      write (*,111)
      L=M/8.0+1
      do 10 K=1,L
         L1=8.0*(K-1)+1
         L2=8.0*K
         if(L2.gt.M) L2=M
         write(*,222) (J,J=L1,L2)
         write (*,333)
            do 20 I=N1,N2
20          write(*,444) I,(X(J,I),J=L1,L2)
         if(L.ne.1.and.K.ne.L.and.L2.ne.M) then
            write (*,555)
         else
            write (*,666)
            return
         endif
10    continue
      return
111   format (1X,
     +'ษออออัออออออออออออออออออออออออออออออออออออออออออออออออออออออออ',
     +'อออออออออออออออออ')
333   format (1X,
     +'วฤฤฤฤลฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤ',
     +'ฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤ')
555   format (1X,
     +'ฬออออุออออออออออออออออออออออออออออออออออออออออออออออออออออออออ',
     +'อออออออออออออออออ')
666   format (1X,
     +'ศออออฯออออออออออออออออออออออออออออออออออออออออออออออออออออออออ',
     +'อออออออออออออออออ')
222   format(1X,'บ    ณ',8(I5,4X))
444   format(1X,'บ',I3,' ณ',8F9.3 )
      end
